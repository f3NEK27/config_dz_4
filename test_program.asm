# Инициализация вектора длиной 5 по адресу 200
# Элементы вектора: 10, 20, 30, 40, 50

# Инициализируем элемент по адресу 200 значением 10
LOAD_CONST 200 2      # R2 = 200 (адрес)
LOAD_CONST 10 3       # R3 = 10 (значение)
WRITE_MEM 3 2 0       # memory[R2 + 0] = R3

# Инициализируем элемент по адресу 201 значением 20
LOAD_CONST 201 2
LOAD_CONST 20 3
WRITE_MEM 3 2 0

# Инициализируем элемент по адресу 202 значением 30
LOAD_CONST 202 2
LOAD_CONST 30 3
WRITE_MEM 3 2 0

# Инициализируем элемент по адресу 203 значением 40
LOAD_CONST 203 2
LOAD_CONST 40 3
WRITE_MEM 3 2 0

# Инициализируем элемент по адресу 204 значением 50
LOAD_CONST 204 2
LOAD_CONST 50 3
WRITE_MEM 3 2 0

# Загружаем число 128 в регистр 5 для операции LEQ
LOAD_CONST 128 5       # R5 = 128

# Выполняем операцию LEQ для каждого элемента вектора

# Элемент 1 (адрес 200)
LOAD_CONST 200 2      # R2 = 200 (адрес элемента)
READ_MEM 3 2          # R3 = memory[R2] (читаем элемент)
LEQ 3 4 5            # R4 = (R3 <= R5) ? 1 : 0
WRITE_MEM 4 2 0       # memory[R2 + 0] = R4

# Элемент 2 (адрес 201)
LOAD_CONST 201 2
READ_MEM 3 2
LEQ 3 4 5
WRITE_MEM 4 2 0

# Элемент 3 (адрес 202)
LOAD_CONST 202 2
READ_MEM 3 2
LEQ 3 4 5
WRITE_MEM 4 2 0

# Элемент 4 (адрес 203)
LOAD_CONST 203 2
READ_MEM 3 2
LEQ 3 4 5
WRITE_MEM 4 2 0

# Элемент 5 (адрес 204)
LOAD_CONST 204 2
READ_MEM 3 2
LEQ 3 4 5
WRITE_MEM 4 2 0

